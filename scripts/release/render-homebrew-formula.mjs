#!/usr/bin/env node
/**
 * Render a complete Homebrew formula for BehalfID standalone release binaries.
 *
 * Usage:
 *   node scripts/release/render-homebrew-formula.mjs \
 *     --version 0.2.9 \
 *     --darwin-arm64-url URL --darwin-arm64-sha256 HEX \
 *     --darwin-x64-url URL --darwin-x64-sha256 HEX \
 *     [--out path]
 */
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";
import { pathToFileURL } from "node:url";

function arg(name, argv = process.argv) {
  const idx = argv.indexOf(`--${name}`);
  if (idx === -1 || !argv[idx + 1]) return undefined;
  return argv[idx + 1];
}

export function renderHomebrewFormula({
  version,
  darwinArm64Url,
  darwinArm64Sha256,
  darwinX64Url,
  darwinX64Sha256,
}) {
  if (!version || !darwinArm64Url || !darwinArm64Sha256 || !darwinX64Url || !darwinX64Sha256) {
    throw new Error(
      "version, darwinArm64Url, darwinArm64Sha256, darwinX64Url, and darwinX64Sha256 are required"
    );
  }
  // Exact stable semantic version only — an unanchored prefix match would
  // accept suffixed values like "0.2.9-beta" or "0.2.9x".
  if (!/^[0-9]+\.[0-9]+\.[0-9]+$/.test(version)) {
    throw new Error(`invalid version: ${version}`);
  }
  for (const [label, sha] of [
    ["darwinArm64Sha256", darwinArm64Sha256],
    ["darwinX64Sha256", darwinX64Sha256],
  ]) {
    if (!/^[a-f0-9]{64}$/i.test(sha)) {
      throw new Error(`${label} must be a 64-char hex SHA-256`);
    }
  }

  const armSha = darwinArm64Sha256.toLowerCase();
  const x64Sha = darwinX64Sha256.toLowerCase();
  // Keep Ruby "#{bin}" out of JS template literals to avoid tooling confusion.
  const rubyBinBehalf = '#{bin}/behalf --version';
  const rubyBinBehalfId = '#{bin}/behalfid --version';

  return [
    "# Homebrew formula for BehalfID CLI (standalone binaries)",
    "# Canonical tap: github.com/BehalfID/homebrew-tap",
    "# Install: brew install BehalfID/tap/behalf",
    "# Generated by scripts/release/render-homebrew-formula.mjs - do not hand-edit live tap copies.",
    "",
    "class Behalf < Formula",
    '  desc "Official CLI for BehalfID - agent permission management"',
    '  homepage "https://behalfid.com"',
    `  version "${version}"`,
    '  license "MIT"',
    "",
    "  on_macos do",
    "    on_arm do",
    `      url "${darwinArm64Url}"`,
    `      sha256 "${armSha}"`,
    "    end",
    "    on_intel do",
    `      url "${darwinX64Url}"`,
    `      sha256 "${x64Sha}"`,
    "    end",
    "  end",
    "",
    "  def install",
    '    bin.install "behalf"',
    '    bin.install_symlink "behalf" => "behalfid"',
    "  end",
    "",
    "  test do",
    `    assert_match version.to_s, shell_output("${rubyBinBehalf}")`,
    `    assert_match version.to_s, shell_output("${rubyBinBehalfId}")`,
    "  end",
    "end",
    "",
  ].join("\n");
}

function main(argv = process.argv) {
  const formula = renderHomebrewFormula({
    version: arg("version", argv),
    darwinArm64Url: arg("darwin-arm64-url", argv),
    darwinArm64Sha256: arg("darwin-arm64-sha256", argv),
    darwinX64Url: arg("darwin-x64-url", argv),
    darwinX64Sha256: arg("darwin-x64-sha256", argv),
  });
  const out = arg("out", argv);
  if (out) {
    mkdirSync(dirname(out), { recursive: true });
    writeFileSync(out, formula);
    console.error(`Wrote ${out}`);
  } else {
    process.stdout.write(formula);
  }
}

const isMain =
  Boolean(process.argv[1]) &&
  import.meta.url === pathToFileURL(process.argv[1]).href;

if (isMain) {
  try {
    main();
  } catch (err) {
    console.error(err instanceof Error ? err.message : String(err));
    process.exit(1);
  }
}
